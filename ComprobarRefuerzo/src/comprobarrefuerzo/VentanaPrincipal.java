    private void ConsultarRefuerzoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarRefuerzoActionPerformed
        Pair<Pair<Integer,Integer>,Integer> refuerzoTrabajado= new Pair();
        this.jTiempoRefuerzo.setText(null);
        if(this.RefuerzoFechaManual.isSelected()){
            if(anhoConsultaRefuerzo.getText().isEmpty()==false ){
                this.anhoConsultaRefuerzo.setVisible(true);
                this.diaConsultaRefuerzo.setVisible(true);
                this.MesConsultaRefuerzo.setVisible(true);
                refuerzoTrabajado=programa.numeroMesesRefuerzo(diaConsultaRefuerzo.getSelectedIndex()+1, programa.obtenerNumeroMes(MesConsultaRefuerzo.getSelectedItem().toString()),Integer.parseInt(anhoConsultaRefuerzo.getText()));
                this.jTiempoRefuerzo.setText(null);
                this.jTiempoRefuerzo.append("Tienes "+  refuerzoTrabajado.getFirst().getFirst() + " dias y " + refuerzoTrabajado.getFirst().getSecond() + " meses y "
                         + refuerzoTrabajado.getSecond() +" años\n");
            }else{
                new ErrorIntroduccionTipoContrato(this , true ).setVisible(true);
            }  
        }else if(this.RefuerzoFechaAuto.isSelected()){
            refuerzoTrabajado=programa.numeroMesesRefuerzo();
            this.jTiempoRefuerzo.setText(null);
            this.jTiempoRefuerzo.append("Tienes "+  refuerzoTrabajado.getFirst().getFirst() + " dias y " + refuerzoTrabajado.getFirst().getSecond() + " meses y "
                         + refuerzoTrabajado.getSecond() +" años\n");
        }
    }//GEN-LAST:event_ConsultarRefuerzoActionPerformed

