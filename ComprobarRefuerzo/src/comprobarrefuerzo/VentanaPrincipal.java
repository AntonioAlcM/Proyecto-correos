    private void ahnadirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ahnadirMouseClicked
        int diaComienzo= Integer.parseInt(dia_comienzo.getSelectedItem().toString());
        int diaFinalizacion=Integer.parseInt(dia_finalizacion.getSelectedItem().toString());
        String mesComienzo=mes_comienzo.getSelectedItem().toString();
        String mesFinalizacion=mes_finalizacion.getSelectedItem().toString();
        if(programa.sePuedeAnhadir(diaComienzo,diaFinalizacion,mesComienzo,mesFinalizacion,anho_comienzo.getText() , anho_finalizacion.getText()) 
                && programa.comprobarDiaSegunMes(diaFinalizacion, programa.obtenerNumeroMes(mesFinalizacion), Integer.parseInt(anho_finalizacion.getText()))
                &&programa.comprobarDiaSegunMes(diaComienzo, programa.obtenerNumeroMes(mesComienzo), Integer.parseInt(anho_comienzo.getText()))){
                int numeroTipoContrato=0;
                Contrato contrato= new Contrato(diaComienzo,diaFinalizacion, mesComienzo, mesFinalizacion,anho_comienzo.getText() , anho_finalizacion.getText());
                if (this.TipoContratoRefuerzo.isSelected() && programa.BuscarRepetidos(contrato).getFirst()==false ){
                    ficheroRefuerzo.delete();
                    numeroTipoContrato=1;
                    programa.setContrato(numeroTipoContrato, contrato, ficheroRefuerzo);
                       
                }else if(this.TipoContratoVacaciones.isSelected() && programa.BuscarRepetidos(contrato).getFirst()==false){
                    ficheroVacaciones.delete();
                    numeroTipoContrato=2;
                    programa.setContrato(numeroTipoContrato, contrato, ficheroVacaciones);
                }else if(this.TipoContratoBaja.isSelected() && programa.BuscarRepetidos(contrato).getFirst()==false){
                    ficheroBajas.delete();
                    numeroTipoContrato=3;
                    programa.setContrato(numeroTipoContrato, contrato, ficheroBajas);
                }else
                    new ErrorIntroduccionTipoContrato(this , true ).setVisible(true);   
        }else
            new ErrorIntroduccionDatos(this , true ).setVisible(true);      
        programa.Ordenar();
        programa.imprimirDatosPaneles();
        programa.imprimirTiempoTrabajado();
        repaint();
    }//GEN-LAST:event_ahnadirMouseClicked
    
    
