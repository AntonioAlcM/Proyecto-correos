    private void JEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JEliminarActionPerformed
        int diaComienzo= this.dia_comienzo_eliminar.getSelectedIndex()+1;
        int diaFinalizacion=dia_finalizacion_eliminar.getSelectedIndex()+1;
        String mesComienzo=mes_comienzo_eliminar.getSelectedItem().toString();
        String mesFinalizacion=mes_finalizacion_eliminar.getSelectedItem().toString();
        Contrato contrato= new Contrato(diaComienzo,diaFinalizacion, mesComienzo, mesFinalizacion,anho_comienzo_eliminar.getText() , anho_finalizacion_eliminar.getText());
        int tipoContrato=0;
        if(programa.BuscarRepetidos(contrato).getFirst()){
            tipoContrato=programa.BuscarRepetidos(contrato).getSecond();
            if(tipoContrato==1){
                ficheroRefuerzo.delete();
                programa.Eliminar(tipoContrato, contrato);
                try {
                    programa.guardaDatos(tipoContrato,ficheroRefuerzo);
                } catch (IOException ex) {
                    Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else if (tipoContrato==2){
                ficheroVacaciones.delete();
                programa.Eliminar(tipoContrato, contrato);
                try {
                    programa.guardaDatos(tipoContrato,ficheroVacaciones);
                } catch (IOException ex) {
                    Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else if(tipoContrato==3){
                ficheroBajas.delete();
                programa.Eliminar(tipoContrato, contrato);
                try {
                    programa.guardaDatos(tipoContrato,ficheroBajas);
                } catch (IOException ex) {
                    Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }else
            new ErrorEliminarContrato(this , true ).setVisible(true);
        programa.imprimirDatosPaneles();
        programa.imprimirTiempoTrabajado();
    }//GEN-LAST:event_JEliminarActionPerformed

